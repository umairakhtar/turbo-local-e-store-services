    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblPrice.getSelectedRow();
        if( selectedRow >= 0 ) {  
        DeliveryPrice dp=(DeliveryPrice)tblPrice.getValueAt(selectedRow, 0);
        UpdateDeliveryPrice cd = new UpdateDeliveryPrice(userProcessContainer, enterprise, dp);
        userProcessContainer.add("EnsureDelivery",cd);
        CardLayout layout=(CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        else{
               JOptionPane.showMessageDialog(null, "Please select a row!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

