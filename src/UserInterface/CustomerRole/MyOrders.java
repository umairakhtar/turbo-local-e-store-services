    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrders.getSelectedRow();
        if (selectedRow >= 0) {
            if (radioProduct.isSelected()) {
                Order or = (Order) tblOrders.getValueAt(selectedRow, 0);
                ViewOrder viewOrder = new ViewOrder(userProcessContainer, account, or);
                userProcessContainer.add("viewOrder", viewOrder);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            } else {
                Service ser = (Service) tblOrders.getValueAt(selectedRow, 0);
                ViewServiceOrder viewServiceOrder = new ViewServiceOrder(userProcessContainer, account, ser);
                userProcessContainer.add("viewServiceOrder", viewServiceOrder);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnViewActionPerformed

