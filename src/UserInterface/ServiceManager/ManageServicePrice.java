    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       int selectedRow = tblPrice.getSelectedRow();
        if( selectedRow >= 0 ) {  
        ServicePrice sp=(ServicePrice)tblPrice.getValueAt(selectedRow, 0);
        UpdateServicePrice update = new UpdateServicePrice(userProcessContainer, enterprise, serviceOrganization,sp);
        userProcessContainer.add("Update",update);
        CardLayout layout=(CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        else{
               JOptionPane.showMessageDialog(null, "Please select a row!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    
    }//GEN-LAST:event_btnUpdateActionPerformed

