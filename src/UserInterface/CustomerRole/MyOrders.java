    public final Network whatNetwork() {
        Network n = null;
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise == this.enterprise) {
                    n = network;
                    break;
                }
            }
        }
        return n;
    }

    public void populateProductTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblOrders.getModel();
        dtm.setRowCount(0);
        Object row[] = new Object[5];
        for (Order order : ((CustomerOrganization) organization).getOrderList().getOrders()) {
            if (order.getUserAccount() == account) {
                row[0] = order;
                row[1] = order.getDate();
                dtm.addRow(row);
            }
        }
    }

    public void populateServiceTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblOrders.getModel();
        dtm.setRowCount(0);
        Object row[] = new Object[4];
        for (Service service : ((CustomerOrganization) organization).getServiceList().getServices()) {
            if (service.getUserAccount() == account) {
                row[0] = service;
                row[1] = service.getDate();
                for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if (o instanceof ServiceOrganization) {
                        for (WorkRequest wk : o.getWorkQueue().getWorkRequestList()) {
                            if (wk instanceof ServiceBillingWorkRequest) {
                                if (((ServiceBillingWorkRequest) wk).getService() == service) {
                                    row[2] = wk.getStatus();
                                }
                            }
                        }
                    }
                }

                dtm.addRow(row);
            }
        }
    }

