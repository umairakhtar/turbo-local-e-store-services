    public void populateTable(){
         DefaultTableModel dtm = (DefaultTableModel) tblService.getModel();
        dtm.setRowCount(0);
       for(WorkRequest wk: serviceOrganization.getWorkQueue().getWorkRequestList()){
           if(wk instanceof ServicePlaceWorkRequest){
               if(((ServicePlaceWorkRequest) wk).getEmployee()== account.getEmployee()){
                   Object row[]=new Object[6];
                   row[0]=wk;
                   row[1]=((ServicePlaceWorkRequest) wk).getService().getProblem();
                   row[2]=((ServicePlaceWorkRequest) wk).getService().getDate();
                   row[3]=((ServicePlaceWorkRequest) wk).getService().getHours();
                   row[4]=((ServicePlaceWorkRequest) wk).getService().getAddress();
                   row[5]=wk.getStatus();
                   dtm.addRow(row);
               }
           }
       }
    }
