    public void populateTable(){
         DefaultTableModel model = (DefaultTableModel) tblPrice.getModel();
        model.setRowCount(0);
       if(serviceOrganization.getServicePrices()!= null){
           for(ServicePrice dp:serviceOrganization.getServicePrices().getServicePrices()){
           Object row[]=new Object[3];
           row[0]=dp;
           row[1]=dp.getPrice();
           row[2]=dp.getEquipmentPrice();
           model.addRow(row);
           }
       }
        
        
    }
