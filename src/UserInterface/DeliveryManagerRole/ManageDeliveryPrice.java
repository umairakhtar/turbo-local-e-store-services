   public void populateTable(){
       DefaultTableModel model = (DefaultTableModel) tblPrice.getModel();
        model.setRowCount(0);
       if(deliveryOrganization.getPrice() != null){
           for(DeliveryPrice dp:deliveryOrganization.getPrice().getDeliveryPriceCatalog()){
           Object row[]=new Object[3];
           row[0]=dp;
           row[1]=dp.getStanderedPrice();
           row[2]=dp.getPerPrice();
           model.addRow(row);
           }
       }
   }
