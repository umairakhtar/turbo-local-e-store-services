    private void btnFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblService.getSelectedRow();
         int eqty=(Integer)spinnerQuantity.getValue();
        if (selectedRow >= 0 ) {
            ServicePlaceWorkRequest sp =(ServicePlaceWorkRequest)tblService.getValueAt(selectedRow, 0);
            sp.setStatus("finished");
            sp.setEquipmentQty(eqty);
            populateTable();
           JOptionPane.showMessageDialog(null, "Finish the work");
        }
         else {
            JOptionPane.showMessageDialog(null, "Please select a row!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnFinishActionPerformed


